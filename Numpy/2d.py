import numpy as np
a=np.array([[1,2,3,4,5],[2,4,5,6,7],[3,5,7,8,0]])
print(a)          # (3,5)
print(a.shape)    # order of a matrix



# total no of elements

#(3,4)--> 12 elements
# (5,4)---> 20 elements

# size function :  used to find total number of elements in a matrix
